export function getRandomTemplate(): Omit<ReflectionTemplate, 'id'> {
  const randomIndex = Math.floor(Math.random() * defaultReflectionTemplates.length);
  return defaultReflectionTemplates[randomIndex];
}

export function getTemplatesByCategory(category: 'funny' | 'serious' | 'cute'): Omit<ReflectionTemplate, 'id'>[] {
  return defaultReflectionTemplates.filter(template => template.category === category);
}
